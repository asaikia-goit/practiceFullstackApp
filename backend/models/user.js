import mongoose from 'mongoose';

const userScheme = new mongoose.Schema({
    userId: {
        type: String,
        required: true,
        trim: true,
    },
    email: {
        type: String,
        required: true,
        trim: true,
    },
    firstName: {
        type: String,
        required: true,
        trim: true,
    },
    lastName: {
        type: String,
        required: true,
        trim: true,
    },
    password: {
        type: String,
        required: true,
        trim: true,
    },
});

const Users = mongoose.model('Users', userScheme);
export default Users;
